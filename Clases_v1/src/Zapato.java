
public class Zapato {
// Atributos
	String marca;
	String modelo;
	double precio;
	boolean conCordones;
	
// Constructores
	Zapato() {}


	Zapato(String marca, String modelo, double precio, boolean conCordones) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.precio = precio;
		this.conCordones = conCordones;
	}
}
