
public class Arcoiris {
// Atributos
	String tipoDeArcoiris;
	boolean rojo;
	boolean naranja;
	boolean amarillo;
	boolean verde;
	boolean azul;
	
	
	Arcoiris() {}


	Arcoiris(String tipoDeArcoiris, boolean rojo, boolean naranja, boolean amarillo, boolean verde, boolean azul) {
		super();
		this.tipoDeArcoiris = tipoDeArcoiris;
		this.rojo = rojo;
		this.naranja = naranja;
		this.amarillo = amarillo;
		this.verde = verde;
		this.azul = azul;
	}
}
